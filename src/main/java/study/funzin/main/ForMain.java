public class ForMain {

    public static void main(String [] args){

        int last = 5;
        for (int i = last; i >  0; i--) {
            System.out.println(i);
        }
        System.out.println();
        for (int i = 0; i < last; i++) {
            System.out.println(i);
        }

    }

}
